import java.util.*;
public class A2 {
    public static void main(String[] args)
    {
        var c=0;
        System.out.println("The value of c initially is: "+c);
        for(int i=0;i<10;i++)
        {
            c++;
        }
        System.out.println("The value of c after incrementing is: "+c);
        System.out.println("The type of c is: int");
    }
}
